    /**
    * Maneja el evento de búsqueda de un técnico en la base de datos.
    * Al hacer clic en el botón "Buscar", este método habilita los controles de la interfaz que permiten la modificación
    * (botones "Modificar" y "Eliminar") y la visualización de los datos del técnico.
    * Luego, obtiene el número de DNI ingresado por el usuario desde el campo de texto correspondiente.
    * Utiliza el objeto 'crudtecnico' para buscar un técnico en la base de datos con ese DNI y muestra los resultados
    * en los campos de texto de nombre, apellido, teléfono, estado y ID.
    * 
    * @param evt Evento de acción que desencadena la ejecución del método, en este caso, el clic en el botón "Buscar".
    */
    private void btneditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneditarActionPerformed
        String dni = txtdni.getText();
        if (dni.matches("^[0-9]+$")) {
            this.habilitarCampos();
        
            this.crudtecnico.consultar(dni, txtid, txtnombre, txtapellido, txttelefono, cbestado);
        } else {
            JOptionPane.showMessageDialog(null,"Por favor ingrese un dni valido con el formato 0-9");
        }
        
    }//GEN-LAST:event_btneditarActionPerformed

