    /**
    * Maneja el evento de modificación de datos de un técnico en la base de datos.
    * Cuando se hace clic en el botón "Modificar", este método obtiene los datos ingresados por el usuario
    * desde los campos de texto (nombre, apellido, teléfono) y ComboBox (estado) de la interfaz.
    * Luego, se utiliza el número de DNI ingresado en el campo correspondiente para identificar al técnico
    * que se va a modificar. Utiliza el objeto 'crudtecnico' para actualizar los datos del técnico en la base de datos
    * con la información proporcionada.
    * Finalmente, se llama al método 'limpiarCampos' para restablecer los campos de entrada de la interfaz.
    * 
    * @param evt Evento de acción que desencadena la ejecución del método, en este caso, el clic en el botón "Modificar".
    */
    private void btnmodificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnmodificarActionPerformed
        String dni = txtdni.getText();
        String nombre = txtnombre.getText();
        String apellido = txtapellido.getText();
        String telefono = txttelefono.getText();
        String e = (String) cbestado.getSelectedItem();
        System.out.println(e);
        boolean estado;
        
        if ("Activo".equals(e)){
            estado = true;
        }else{
            estado = false;
        }
        System.out.println(estado);

        if (nombre.matches("^[A-Za-z]+$") && apellido.matches("^[A-Za-z]+$")) {
            if (telefono.matches("^[0-9]+$")) {
                if (dni.matches("^[0-9]+$")) {
                    this.crudtecnico.modificarTecnico(nombre, apellido, telefono,estado, dni);
                    limpiarCampos();
                    this.deshabilitarCampos();
                } else {
                    JOptionPane.showMessageDialog(null, "Formato de DNI incorrecto.\nFormato: solo se aceptan caracteres de 0-9 sin espacios.");
                }
            } else {
                JOptionPane.showMessageDialog(null, "Formato de teléfono incorrecto.\nFormato: solo se aceptan caracteres de 0-9 sin espacios.");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Formato de nombre y/o apellido incorrecto o falta rellenar algún campo.\nFormato: solo se aceptan caracteres de A-Z sin espacios.");
        }
    }//GEN-LAST:event_btnmodificarActionPerformed

    /**
    * Maneja el evento de eliminación de un técnico de la base de datos.
    * Cuando se hace clic en el botón "Eliminar", este método obtiene el número de DNI
    * ingresado por el usuario en el campo de texto correspondiente de la interfaz.
    * Luego, utiliza el objeto 'crudtecnico' para eliminar al técnico correspondiente
    * de la base de datos utilizando el número de DNI proporcionado.
    * Finalmente, se llama al método 'limpiarCampos' para restablecer los campos de entrada de la interfaz.
    * 
    * @param evt Evento de acción que desencadena la ejecución del método, en este caso, el clic en el botón "Eliminar".
    */
    private void btneliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneliminarActionPerformed
        String dni = txtdni.getText();
        this.crudtecnico.eliminarTecnico(dni);
        this.limpiarCampos();
        this.deshabilitarCampos();
    }//GEN-LAST:event_btneliminarActionPerformed

    private void limpiarCampos(){
        txtid.setText("");
        txtnombre.setText("");
        txtapellido.setText("");
        cbestado.setSelectedItem(null);
        txttelefono.setText("");
        txtdni.setText("");
    }
    
