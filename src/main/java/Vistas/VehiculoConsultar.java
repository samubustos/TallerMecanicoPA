    /**
    * Maneja el evento de búsqueda de un vehículo en la base de datos según la patente seleccionada en el ComboBox de patentes.
    * Al hacer clic en el botón "Buscar", este método habilita los campos de edición de marca, modelo y estado, así como los botones
    * de modificar y eliminar. Luego, utiliza el objeto 'crudvehiculo' para consultar los datos del vehículo correspondiente a la patente
    * seleccionada y muestra estos datos en los campos de edición de marca, modelo y estado.
    * 
    * @param evt Evento de acción que desencadena la ejecución del método, en este caso, el clic en el botón "Buscar".
    */
    private void btneditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneditarActionPerformed
        
        String patente =(String) cbpatente.getSelectedItem();
        
        if (patente!=null){
            this.habilitarCampos();
            try {
                this.crudvehiculo.consultarDatos(patente,txtmarca,txtmodelo,cbestado);
            } catch (SQLException ex) {
                Logger.getLogger(VehiculoConsultar.class.getName()).log(Level.SEVERE, null, ex);
            }
        }else{
            JOptionPane.showMessageDialog(null,"Por favor seleccione una patente.","Mensaje de Error",JOptionPane.INFORMATION_MESSAGE);
        }

        
    }//GEN-LAST:event_btneditarActionPerformed

