    /**
    * Maneja el evento de modificación de los datos de un vehículo en la base de datos.
    * Al hacer clic en el botón "Modificar", este método obtiene la patente y el estado seleccionados en los ComboBox correspondientes.
    * Luego, utiliza el objeto 'crudvehiculo' para actualizar el estado del vehículo en la base de datos según la patente seleccionada.
    * Después de la modificación, se llama al método 'limpiarCampos' para restablecer los campos y controles de la interfaz a su estado inicial.
    * 
    * @param evt Evento de acción que desencadena la ejecución del método, en este caso, el clic en el botón "Modificar".
    */
    private void btnmodificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnmodificarActionPerformed
        
        String patente = (String) cbpatente.getSelectedItem();
        String e = (String) cbestado.getSelectedItem();
        boolean estado;
        if (e.equals("Activo")){
            estado = true;
        }else{
            estado = false;
        }
        this.crudvehiculo.modificar(patente, estado);
        this.limpiarCampos();
        this.deshabilitarCampos();
    }//GEN-LAST:event_btnmodificarActionPerformed

    /**
    * Maneja el evento de eliminación de un vehículo de la base de datos.
    * Al hacer clic en el botón "Eliminar", este método obtiene la patente seleccionada en el ComboBox de patentes.
    * Luego, utiliza el objeto 'crudvehiculo' para eliminar el vehículo correspondiente en la base de datos según la patente seleccionada.
    * Después de la eliminación, se llama al método 'limpiarCampos' para restablecer los campos y controles de la interfaz a su estado inicial.
    * 
    * @param evt Evento de acción que desencadena la ejecución del método, en este caso, el clic en el botón "Eliminar".
    */
    private void btneliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneliminarActionPerformed
        String patente = (String) cbpatente.getSelectedItem();
        this.crudvehiculo.eliminarVehiculo(patente);
        this.limpiarCampos();
        this.deshabilitarCampos();
    }//GEN-LAST:event_btneliminarActionPerformed

    /**
    * Resetea los campos.
    */
    public void limpiarCampos(){
        
        txtmarca.setText("");
        txtmodelo.setText("");
        
        this.crudvehiculo.cargarPatentes(cbpatente);
        cbpatente.setSelectedItem(null);
        cbestado.setSelectedItem(null);
    }
    
    
